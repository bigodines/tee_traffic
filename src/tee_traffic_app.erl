-module(tee_traffic_app).

-behaviour(application).

-export([start/2,
         stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    {ok, _} = ranch:start_listener(tee_traffic,
                                   10,
                                   ranch_tcp,
                                   [{port, 5555}],
                                   split_traffic_protocol,
                                   []),
    tee_traffic_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

